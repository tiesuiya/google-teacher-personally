package org.lhpsn.base.designpattern;

/**
 * 设计模式六大原则
 *
 * @Author: lihong
 * @Date: 2018/8/10
 * @Description
 */
public class Principle {

    // PS：原则是要有，但也避免过度设计

    // 单一职责原则（SRP,Single Responsibility Principle）
    // 定义：有且仅有一个原因引起类的变更。There should never be more than one reason for a class to change。

    // 开放封闭原则（PCP,Open Close Principle）
    // 定义：对扩展开放，对修改封闭（Open for extension,Close for modification）

    // 依赖倒置原则（DIP）
    // 定义：1.高层模块不应该依赖底层模块。两个都应该依赖抽象。2.抽象不应该依赖细节，细节应该依赖抽象。（面向 接口/抽象 编程）

    // 里斯代换原则（LSP）
    // 定义：子类型必须能够替换掉他们的父类型。
}
